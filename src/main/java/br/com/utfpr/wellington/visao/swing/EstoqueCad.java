    public static void preenchertabela(){
        DefaultTableModel model = (DefaultTableModel) tabela.getModel();
        model.setNumRows(0);
        
        Object colunas[] = new Object[7];
        
        EstoqueRN estoqueRN = new EstoqueRN();
        
        List<Estoque> estoques = estoqueRN.listarEstoque();
        
        for (Estoque estoque : estoques) {
            
            colunas[0] = estoque.getId();
            colunas[1] = estoque.getBebidas().getNome();
            colunas[2] = estoque.getDataCompra();
            colunas[3] = estoque.getQtdTotal();
            colunas[4] = estoque.getValorLote();
            colunas[5] = estoque.getBebidas().getTipo();
            colunas[6] = estoque.getBebidas().getValidade();
            model.addRow(colunas);
        } 
    }

