    private void cadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastroActionPerformed
        
        Bebidas bebidas = new Bebidas();
        Estoque estoque = new Estoque();
        
        try {
            bebidas.setNome(nome.getText());
            bebidas.setPreco(Float.parseFloat(vVenda.getText()));
            switch (tipoB.getSelectedIndex()) {
                case 1:
                    bebidas.setTipo("Suco");
                    break;
                case 2:
                    bebidas.setTipo("Cerveja");
                    break;
                case 3:
                    bebidas.setTipo("Refrigerante");
                    break;
                default:
                    break;
            }
            bebidas.setValidade(validade.getText());
            
            estoque.setBebidas(bebidas);
            estoque.setDataCompra(dtCompra.getText());
            estoque.setQtdTotal(Integer.parseInt(qtd.getText()));
            estoque.setValorLote(Float.parseFloat(vLote.getText()));
            
            GenericDAO<Estoque> genericDao = new GenericDAOimpl<Estoque>(){};
            genericDao.Cadastrar(estoque);
            JOptionPane.showMessageDialog(null,"O cadastro foi realizado com sucesso!");
            
            preenchertabela();          
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Preencha as Informações do Cadastro Corretamente");
        }
        
    }//GEN-LAST:event_cadastroActionPerformed

